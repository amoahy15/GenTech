class Artwork:
    def __init__(self, artworkID, title, artist, year, description, image_location, image_url, annotations):
        self.artworkID = artworkID
        self.title = title
        self.artist = artist
        self.year = year
        self.description = description
        self.image_location = image_location
        self.image_url = image_url
        self.annotations = annotations
